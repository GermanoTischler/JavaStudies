public class Numeric {

    public static void main(String[] args){
        // NUMERIC TYPE

        int a;
        int b = 2;
        a = 3;
        int add = a + b;
        int sub = a - b;
        int mul = a * b;
        float div = (float) a / b;
        System.out.println(add);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
    }
}
